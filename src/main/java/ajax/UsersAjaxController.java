    /**
     * ÌöåÏõê ÌÉàÌá¥ Ï≤òÎ¶¨
     */
    private void handleDeleteUser(HttpServletRequest request, HttpServletResponse response)
            throws IOException {

        HttpSession session = request.getSession(false);
        if (session == null || session.getAttribute("user") == null) {
            response.setStatus(HttpServletResponse.SC_UNAUTHORIZED);
            return;
        }

        Users user = (Users) session.getAttribute("user");
        String password = request.getParameter("password");

        Map<String, Object> result = new HashMap<>();

        try {
            if (usersService.deleteUser(user.getUserId(), password)) {
                session.invalidate();
                result.put("success", true);
                result.put("message", "ÌöåÏõê ÌÉàÌá¥Í∞Ä ÏôÑÎ£åÎêòÏóàÏäµÎãàÎã§.");
            } else {
                result.put("success", false);
                result.put("message", "ÌöåÏõê ÌÉàÌá¥ Ï≤òÎ¶¨Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.");
            }
        } catch (UsersService.ServiceException e) {
            result.put("success", false);
            result.put("message", e.getMessage());
        }

        PrintWriter out = response.getWriter();
        out.print(gson.toJson(result));
        out.flush();
    }
    
    
